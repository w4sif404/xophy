#!/usr/bin/env python3

# ANSI escape codes for terminal colors
RESET = "\033[0m"
BOLD = "\033[1m"
DIM = "\033[2m"
ITALIC = "\033[3m"
UNDERLINE = "\033[4m"
REVERSE = "\033[7m"
STRIKETHROUGH = "\033[9m"

# Regular colors
BLACK = "\033[30m"
RED = "\033[31m"
GREEN = "\033[32m"
YELLOW = "\033[33m"
BLUE = "\033[34m"
MAGENTA = "\033[35m"
CYAN = "\033[36m"
WHITE = "\033[37m"

# Bright colors
BRIGHT_BLACK = "\033[90m"
BRIGHT_RED = "\033[91m"
BRIGHT_GREEN = "\033[92m"
BRIGHT_YELLOW = "\033[93m"
BRIGHT_BLUE = "\033[94m"
BRIGHT_MAGENTA = "\033[95m"
BRIGHT_CYAN = "\033[96m"
BRIGHT_WHITE = "\033[97m"

# Aliases
LIGHT_GRAY = BRIGHT_BLACK
DARK_GRAY = "\033[2m\033[37m"
LIGHT_RED = BRIGHT_RED
LIGHT_GREEN = BRIGHT_GREEN
LIGHT_YELLOW = BRIGHT_YELLOW
LIGHT_BLUE = BRIGHT_BLUE
LIGHT_MAGENTA = BRIGHT_MAGENTA
LIGHT_CYAN = BRIGHT_CYAN

# Background colors
BG_BLACK = "\033[40m"
BG_RED = "\033[41m"
BG_GREEN = "\033[42m"
BG_YELLOW = "\033[43m"
BG_BLUE = "\033[44m"
BG_MAGENTA = "\033[45m"
BG_CYAN = "\033[46m"
BG_WHITE = "\033[47m"

# Special
CLEAR = "\033[2J"
CLEAR_LINE = "\033[2K"
CURSOR_UP = "\033[A"
CURSOR_DOWN = "\033[B"
CURSOR_RIGHT = "\033[C"
CURSOR_LEFT = "\033[D"
SAVE_CURSOR = "\033[s"
RESTORE_CURSOR = "\033[u"
HIDE_CURSOR = "\033[?25l"
SHOW_CURSOR = "\033[?25h"
